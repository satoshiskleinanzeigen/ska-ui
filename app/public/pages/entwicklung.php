<h2>Aktueller Stand des Projekts und Probleme</h2>
Zur Zeit können bereits Anzeigen über ein Formular auf unserer Website inseriert werden.

Wir arbeiten aktuell an folgenden wichtigen Features:
<ul class="chevronlist">
	<li>Eigenverantwortung fördern.</li>
	<li>User sollen Anzeigen selbstständig löschen und bearbeiten können</li>
	<li>Login per Lightning</li>
	<li>Bewertungen</li>
</ul>

Es gab Anfang 2023 einen Betrugsfall, bei dem einige Plebs um ihre Satoshis betrogen wurden. Wir möchten auf der Website Bewertungen einführen, damit für Käufer ersichtlich ist, welchen Verkäufer man Vertrauen schenken kann.
<br /><br />
Damit die Bewertungen nicht gefälscht werden können, werden sie Sats und damit einen Arbeitsnachweis kosten, die zum Teil an das Satoshis Kleinanzeigen Team fließen.
So möchten wir das Projekt monetarisieren ohne eine Einstellgebühr oder eine Provision zu nehmen, denn dies würde Fehlanreize schaffen.
Wie viele Sats eine Bewertung wert ist entscheidet der Nutzer, die Inserate werden dann auf der Startseite nach Anzahl gespendeter Sats und Anzahl Bewertungen sortiert.
Interessenten wird ein 'Trust-Score' angezeigt um zu erkennen wie viel Satoshis ein Account wert ist und somit einfach abschätzen ob Sie dem Anbieter bei diesem Handel vertrauen können.
Diese Spieltheorie ist dem Proof of Work und dem Bieten um Speicherplatz in der Blockchain von Bitcoin abgeschaut.


<br /><br /><br />
<h2>Zeitplan</h2>
2023 bauen wir eine Website um die UX zu verbessern, sowie ein Bewertungssystem um Spam und Betrüger abzuwehren.
2024-2025 möchten wir spätestens daran arbeiten, das Projekt zu einem dezentralem Protokoll zu entwickeln, sodass es nicht nur einen zentralen Server gibt, sondern wie bei Nostr jeder Pleb einen Server stellen und monetarisieren kann
Die ist förderlich für die Werte-Diversität und Zensur-Resistenz der Marktplätze.

