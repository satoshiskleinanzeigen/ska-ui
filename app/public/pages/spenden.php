<h2>Warum SatoshisKleinanzeigen und nicht andere Top-Down Projekte</h2>
<strong>Satoshis Kleinanzeigen</strong> ist eine Bottom-Up Bewegegung, angetrieben durch die Begeisterung Bitcoin auf Herz und Nieren zu testen.
Die Mitglieder sind eng miteinander vernetzt, sodass jetzt schon ein frequenter Handel stattfindet.
<br /><br />
Wir möchten an einem Marktplatz-Protokoll arbeiten, um eine Alternative zu zentral wuchernden Plattformen zu bieten.<br />
Auf Nostr gibt es bereits einen NIP (Nostr Improvment Proposal) für zensurresistente Marktplätze.
Wir möchten Satoshis Kleinanzeigen langfristig dort einbinden.
