<h2>Aktueller Stand des Projekts und Probleme</h2>
Zur Zeit können in einer Telegram Gruppe Anzeigen als Nachricht inseriert werden, diese werden manuell von Admins auf Form und Inhalt geprüft und in einen Telegram Kanal weitergeleitet.
Dies ist sehr aufwendig und bald vom Arbeitsaufwand nicht mehr tragbar, außerdem ist es nicht sehr intuitiv Inserate in der richtigen Form als Nachricht einzustellen.
Deshalb arbeiten wir gerade an einer Website, auf der Inserate über eine Eingabemaske eingestellt werden können.
Diese werden wir spätestens Sommer 2023 veröffentlichen.<br /><br />
Die Website steht schon, es muss jedoch noch viel getestet und repariert werden, sowie einen stabilen Server angemietet werden. 
<br /><br />
Es gab Anfang 2023 einen Betrugsfall, bei dem einige Plebs um ihre Satoshis betrogen wurden. Wir möchten auf der Website Bewertungen einführen, damit für Käufer ersichtlich ist, welchen Verkäufer man Vertrauen schenken kann.
Damit die Bewertungen nicht gefälscht werden können, werden sie Sats und damit einen Arbeitsnachweis kosten, die zum Teil an das Satoshis Kleinanzeigen Team fließen.
So möchten wir das Projekt monetarisieren ohne eine Einstellgebühr oder eine Provision zu nehmen, denn dies würde Fehlanreize schaffen.
Wie viele Sats eine Bewertung wert ist entscheidet der Nutzer, die Inserate werden dann auf der Startseite nach Anzahl gespendeter Sats und Anzahl Bewertungen sortiert.
Diese Spieltheorie ist dem Proof of Work und dem Bieten um Speicherplatz in der Blockchain von Bitcoin abgeschaut.

